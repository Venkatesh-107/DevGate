// ─── Device Flow OAuth Dialog ─────────────────────────────────────────────────

class _DeviceFlowDialog extends StatefulWidget {
  final GitHubClient githubClient;
  final void Function(String token) onSuccess;

  const _DeviceFlowDialog({
    required this.githubClient,
    required this.onSuccess,
  });

  @override
  State<_DeviceFlowDialog> createState() => _DeviceFlowDialogState();
}

class _DeviceFlowDialogState extends State<_DeviceFlowDialog> {
  _FlowStep _step = _FlowStep.loading;
  String _userCode = '';
  String _verificationUri = '';
  String _errorMessage = '';

  @override
  void initState() {
    super.initState();
    _startFlow();
  }

  Future<void> _startFlow() async {
    try {
      final data = await widget.githubClient.requestDeviceCode();
      if (!mounted) return;

      setState(() {
        _userCode        = data['user_code'] ?? '';
        _verificationUri = data['verification_uri'] ?? 'https://github.com/login/device';
        _step            = _FlowStep.waitingForUser;
      });

      // Poll in background.
      final deviceCode = data['device_code'] as String;
      final interval   = (data['interval'] as int?) ?? 5;
      final token = await widget.githubClient.pollForToken(deviceCode, interval);

      if (!mounted) return;
      if (token != null) {
        widget.onSuccess(token);
        Navigator.of(context).pop();
      } else {
        setState(() {
          _step = _FlowStep.error;
          _errorMessage = 'Authorization failed or expired. Please try again.';
        });
      }
    } catch (e) {
      if (!mounted) return;
      setState(() {
        _step = _FlowStep.error;
        _errorMessage = 'Could not connect to GitHub: $e';
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    return AlertDialog(
      backgroundColor: const Color(0xFF1E1E1E),
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      title: const Row(
        children: [
          Icon(Icons.login, color: Color(0xFF8AB4F8)),
          SizedBox(width: 10),
          Text('Sign in with GitHub',
              style: TextStyle(color: Colors.white, fontSize: 18)),
        ],
      ),
      content: SizedBox(
        width: 380,
        child: _buildContent(),
      ),
      actions: [
        TextButton(
          onPressed: () => Navigator.of(context).pop(),
          child: const Text('Cancel', style: TextStyle(color: Colors.grey)),
        ),
      ],
    );
  }

  Widget _buildContent() {
    switch (_step) {
      case _FlowStep.loading:
        return const SizedBox(
          height: 120,
          child: Center(
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                CircularProgressIndicator(color: Color(0xFF8AB4F8)),
                SizedBox(height: 16),
                Text('Connecting to GitHub...',
                    style: TextStyle(color: Colors.grey)),
              ],
            ),
          ),
        );

      case _FlowStep.waitingForUser:
        return Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const Text(
              '1. Copy the code below\n2. Open the link\n3. Enter the code on GitHub',
              style: TextStyle(color: Colors.white70, fontSize: 13, height: 1.5),
            ),
            const SizedBox(height: 20),
            // User code display
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 16),
              decoration: BoxDecoration(
                color: const Color(0xFF8AB4F8).withValues(alpha: 0.1),
                borderRadius: BorderRadius.circular(12),
                border: Border.all(
                    color: const Color(0xFF8AB4F8).withValues(alpha: 0.4)),
              ),
              child: Text(
                _userCode,
                style: const TextStyle(
                  color: Color(0xFF8AB4F8),
                  fontSize: 28,
                  fontWeight: FontWeight.bold,
                  fontFamily: 'monospace',
                  letterSpacing: 6,
                ),
              ),
            ),
            const SizedBox(height: 20),
            SizedBox(
              width: double.infinity,
              child: ElevatedButton.icon(
                onPressed: () async {
                  final uri = Uri.parse(_verificationUri);
                  if (await canLaunchUrl(uri)) await launchUrl(uri);
                },
                icon: const Icon(Icons.open_in_new, size: 16),
                label: Text(
                  'Open $_verificationUri',
                  overflow: TextOverflow.ellipsis,
                ),
                style: ElevatedButton.styleFrom(
                  backgroundColor: Colors.green,
                  foregroundColor: Colors.white,
                  padding: const EdgeInsets.symmetric(vertical: 12),
                  shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(10)),
                ),
              ),
            ),
            const SizedBox(height: 16),
            const Row(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                SizedBox(
                  width: 14,
                  height: 14,
                  child: CircularProgressIndicator(
                    strokeWidth: 2,
                    color: Colors.grey,
                  ),
                ),
                SizedBox(width: 10),
                Text('Waiting for authorization...',
                    style: TextStyle(color: Colors.grey, fontSize: 12)),
              ],
            ),
          ],
        );

      case _FlowStep.error:
        return Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const Icon(Icons.error_outline, color: Colors.redAccent, size: 48),
            const SizedBox(height: 16),
            Text(
              _errorMessage,
              style: const TextStyle(color: Colors.white70, fontSize: 13),
              textAlign: TextAlign.center,
            ),
          ],
        );
    }
  }
