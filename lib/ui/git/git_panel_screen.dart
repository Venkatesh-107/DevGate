  Widget _buildDynamicRecommendations(GitState git) {
    final scanState = ref.watch(scannerStateProvider);
    final secrets = scanState.findings
        .where((f) => f.type != FindingType.recommendation)
        .toList();
    final criticals =
        secrets.where((f) => f.severity == Severity.critical).toList();

    final cards = <Widget>[];

    if (criticals.isNotEmpty) {
      cards.add(_recCard(
        '🔴 ${criticals.length} Critical Secret(s)!',
        'Found: ${criticals.map((f) => f.label).toSet().join(', ')}. Remove before pushing.',
        Icons.key_off_outlined,
        Colors.red,
      ));
    } else if (secrets.isNotEmpty) {
      cards.add(_recCard(
        '🟠 ${secrets.length} Potential Secret(s)',
        'In: ${secrets.map((f) => f.filePath.split('/').last).toSet().take(3).join(', ')}. Check Scanner tab.',
        Icons.warning_amber_rounded,
        Colors.orange,
      ));
    }

    if (_projectPath != null &&
        !File('$_projectPath/.gitignore').existsSync()) {
      cards.add(_recCard(
        '⚠️ Missing .gitignore',
        'Without it, secrets and build files may be pushed accidentally.',
        Icons.shield_outlined,
        Colors.orange,
      ));
    }

    if (_selectedFiles
        .any((f) => f.startsWith('build/') || f.contains('.dart_tool'))) {
      cards.add(_recCard(
        '🟠 Build Artifacts Staged',
        'build/ and .dart_tool/ are generated. Do not push them.',
        Icons.folder_off_outlined,
        Colors.deepOrange,
      ));
    }

    final msg = _commitMsgCtrl.text.trim();
    if (git.changedFiles.isNotEmpty && msg.isNotEmpty && msg.length < 10) {
      cards.add(_recCard(
        '✏️ Improve Commit Message',
        '"$msg" is very short. Describe what changed clearly.',
        Icons.edit_note,
        const Color(0xFF8AB4F8),
      ));
    }

    if (git.changedFiles.length > 20) {
      cards.add(_recCard(
        '📦 Large Commit Detected',
        '${git.changedFiles.length} files. Consider splitting into smaller commits.',
        Icons.commit,
        Colors.amber,
      ));
    }

    if (cards.isEmpty && secrets.isEmpty) {
      cards.add(_recCard(
        '✅ All Clear!',
        git.changedFiles.isEmpty
            ? 'Working tree is clean. No secrets found.'
            : 'No secrets detected. Safe to push!',
        Icons.verified_outlined,
        Colors.green,
      ));
    }

    return Column(
      children: [
        for (int i = 0; i < cards.length; i++) ...[
          cards[i],
          if (i < cards.length - 1) const SizedBox(height: 10),
        ],
      ],
    );
  }

