  }

  // ─── Commit flow ─────────────────────────────────────────────────────────────

  Widget _buildCommitFlow(GitState git, bool isBusy) {
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: const Color(0xFF1E1E1E),
        borderRadius: BorderRadius.circular(16),
        border: Border.all(color: const Color(0xFF3C4043)),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Header
          Row(
            children: [
              const Icon(Icons.check_circle, color: Colors.green, size: 20),
              const SizedBox(width: 8),
              const Expanded(
                child: Text('Connected to GitHub',
                    style: TextStyle(
                        color: Colors.white,
                        fontSize: 16,
                        fontWeight: FontWeight.bold)),
              ),
              // Branch badge
              Container(
                padding: const EdgeInsets.symmetric(
                    horizontal: 10, vertical: 5),
                decoration: BoxDecoration(
                  color: Colors.green.withValues(alpha: 0.1),
                  borderRadius: BorderRadius.circular(20),
                  border: Border.all(
                      color: Colors.green.withValues(alpha: 0.3)),
                ),
                child: Row(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    const Icon(Icons.account_tree,
                        size: 12, color: Colors.greenAccent),
                    const SizedBox(width: 4),
                    Text(git.currentBranch,
                        style: const TextStyle(
                            color: Colors.greenAccent,
                            fontWeight: FontWeight.bold,
                            fontSize: 12)),
                  ],
                ),
              ),
            ],
          ),
          const SizedBox(height: 20),

          // Changed files
          Row(
            children: [
              const Text('Changed Files:',
                  style: TextStyle(color: Colors.white70, fontSize: 13)),
              const Spacer(),
              if (git.changedFiles.isNotEmpty) ...[
                // Diff button
                TextButton.icon(
                  onPressed: () => _loadDiff(null),
                  icon: const Icon(Icons.difference, size: 14),
                  label: const Text('View Diff',
                      style: TextStyle(fontSize: 12)),
                  style: TextButton.styleFrom(
                      foregroundColor: const Color(0xFF8AB4F8)),
                ),
                TextButton.icon(
                  onPressed: () {
                    setState(() {
                      if (_selectedFiles.length == git.changedFiles.length) {
                        _selectedFiles.clear();
                      } else {
                        _selectedFiles.addAll(
                            git.changedFiles.map((f) => f.path));
                      }
                    });
                  },
                  icon: Icon(
                    _selectedFiles.length == git.changedFiles.length
                        ? Icons.deselect
                        : Icons.select_all,
                    size: 14,
                    color: const Color(0xFF8AB4F8),
                  ),
                  label: Text(
                    _selectedFiles.length == git.changedFiles.length
                        ? 'Deselect All'
                        : 'Select All',
                    style: const TextStyle(
                        color: Color(0xFF8AB4F8), fontSize: 12),
                  ),
                ),
              ],
            ],
          ),
          const SizedBox(height: 8),

          if (git.changedFiles.isEmpty)
            const Text(
              'No changes detected in working tree.',
              style:
                  TextStyle(color: Colors.grey, fontStyle: FontStyle.italic),
            )
          else
            Container(
              constraints: const BoxConstraints(maxHeight: 360),
              decoration: BoxDecoration(
                color: const Color(0xFF131314),
                borderRadius: BorderRadius.circular(8),
              ),
              child: ListView.builder(
                shrinkWrap: true,
                padding: const EdgeInsets.all(8),
                itemCount: git.changedFiles.length,
                itemBuilder: (_, i) {
                  final f = git.changedFiles[i];
                  final isSelected = _selectedFiles.contains(f.path);
                  final statusColor = _statusColor(f.status);

                  return InkWell(
                    onTap: () => setState(() {
                      if (isSelected) {
                        _selectedFiles.remove(f.path);
                      } else {
                        _selectedFiles.add(f.path);
                      }
                    }),
                    onSecondaryTap: () => _loadDiff(f.path),
                    child: Padding(
                      padding: const EdgeInsets.symmetric(
                          vertical: 4, horizontal: 4),
                      child: Row(
                        children: [
                          Checkbox(
                            value: isSelected,
                            activeColor: const Color(0xFF8AB4F8),
                            onChanged: (v) => setState(() {
                              if (v == true) {
                                _selectedFiles.add(f.path);
                              } else {
                                _selectedFiles.remove(f.path);
                              }
                            }),
                          ),
                          Container(
                            padding: const EdgeInsets.symmetric(
                                horizontal: 6, vertical: 2),
                            decoration: BoxDecoration(
                              color: statusColor.withValues(alpha: 0.15),
                              borderRadius: BorderRadius.circular(4),
                            ),
                            child: Text(
                              f.statusLabel,
                              style: TextStyle(
                                  color: statusColor,
                                  fontWeight: FontWeight.bold,
                                  fontSize: 11),
                            ),
                          ),
                          const SizedBox(width: 10),
                          Expanded(
                            child: Text(
                              f.path,
                              style: const TextStyle(
                                  color: Colors.grey,
                                  fontFamily: 'monospace',
                                  fontSize: 12),
                              overflow: TextOverflow.ellipsis,
                            ),
                          ),
                          // Diff icon on hover
                          IconButton(
                            iconSize: 14,
                            tooltip: 'View diff',
                            icon: const Icon(Icons.difference_outlined,
                                color: Colors.white24),
                            onPressed: () => _loadDiff(f.path),
                          ),
                        ],
                      ),
                    ),
                  );
                },
              ),
            ),

          const SizedBox(height: 20),
          _buildTextField(
              'Commit Message', _commitMsgCtrl, Icons.edit_note),
          const SizedBox(height: 20),

          // Action buttons
          Wrap(
            alignment: WrapAlignment.end,
            spacing: 10,
            runSpacing: 10,
            children: [
              if (_selectedFiles.isNotEmpty)
                ElevatedButton.icon(
                  onPressed: isBusy
                      ? null
                      : () async {
                          await ref
                              .read(gitProvider.notifier)
                              .stageFiles(_selectedFiles.toList());
                          setState(() => _selectedFiles.clear());
                          _snack('Selected files staged!');
                        },
                  icon: const Icon(Icons.playlist_add_check, size: 16),
                  label: const Text('Stage Selected'),
                  style: ElevatedButton.styleFrom(
                    backgroundColor:
                        const Color(0xFF8AB4F8).withValues(alpha: 0.15),
                    foregroundColor: const Color(0xFF8AB4F8),
                  ),
                ),
              ElevatedButton.icon(
                onPressed: isBusy
                    ? null
                    : () => ref.read(gitProvider.notifier).stageAll(),
                icon: const Icon(Icons.library_add, size: 16),
                label: const Text('Stage All'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: const Color(0xFF3C4043),
                  foregroundColor: Colors.white,
                ),
              ),
              ElevatedButton.icon(
                onPressed: isBusy
                    ? null
                    : () {
                        if (_commitMsgCtrl.text.trim().isEmpty) {
                          _snack('Enter a commit message first.');
                          return;
                        }
                        ref
                            .read(gitProvider.notifier)
                            .commit(_commitMsgCtrl.text.trim());
                        _commitMsgCtrl.clear();
                      },
                icon: const Icon(Icons.check, size: 16),
                label: const Text('Commit'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: Colors.orangeAccent,
                  foregroundColor: Colors.black,
                ),
              ),
              ElevatedButton.icon(
                onPressed: isBusy ? null : _safePush,
                icon: const Icon(Icons.cloud_upload, size: 16),
                label: const Text('Safe Push'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: const Color(0xFF8AB4F8),
                  foregroundColor: Colors.white,
                ),
              ),
              ElevatedButton.icon(
                onPressed: isBusy
                    ? null
                    : () => ref.read(gitProvider.notifier).pull(),
                icon: const Icon(Icons.cloud_download, size: 16),
                label: const Text('Pull'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: Colors.green,
                  foregroundColor: Colors.white,
                ),
              ),
            ],
          ),
        ],
      ),
    );
