class GitPanelScreen extends ConsumerStatefulWidget {
  const GitPanelScreen({super.key});

  @override
  ConsumerState<GitPanelScreen> createState() => _GitPanelScreenState();
}

class _GitPanelScreenState extends ConsumerState<GitPanelScreen> {
  final _commitMsgCtrl    = TextEditingController();
  final _ownerCtrl        = TextEditingController();
  final _repoCtrl         = TextEditingController();
  final _newRepoNameCtrl  = TextEditingController();
  final _storage          = const FlutterSecureStorage();
  final _githubClient     = GitHubClient();

  String? _projectPath;
  String  _githubUsername = '';
  int     _activeTab      = 0; // 0 = Link Existing, 1 = Create New
  bool    _isPrivate      = true;
  bool    _showDiff       = false;
  String? _diffFile;
  final Set<String> _selectedFiles = {};

  @override
  void initState() {
    super.initState();
    _loadProfile();
  }

  Future<void> _loadProfile() async {
    final username = await storageService.getUsername();
    if (username != null) {
      setState(() => _githubUsername = username);
      _ownerCtrl.text = username;
    }
  }

  AppLogger get _logger => ref.read(appLoggerProvider.notifier);

  // ─── Project ────────────────────────────────────────────────────────────────

  Future<void> _pickProject() async {
    try {
      final dir = await getDirectoryPath();
      if (dir != null) {
        setState(() {
          _projectPath  = dir;
          _selectedFiles.clear();
          _showDiff     = false;
          _diffFile     = null;
        });
        await ref.read(gitProvider.notifier).loadProject(dir);
        await _ensureGitignore(dir);
      }
    } catch (e) {
      _snack('Error picking folder: $e');
    }
  }

  Future<void> _ensureGitignore(String dirPath) async {
    final file = File('$dirPath/.gitignore');
    if (!await file.exists()) {
      await file.writeAsString(
        '# Standard .gitignore generated by DevGate\n'
        '.env\n.env.local\nbuild/\n.dart_tool/\n.pub-cache/\n.packages\n'
        '*.log\nnode_modules/\n*.key\n*.pem\n',
      );
      _snack('Created a default .gitignore to protect secrets.');
      _logger.success('Auto-generated .gitignore at $dirPath');
    }
  }

  // ─── Repo setup ──────────────────────────────────────────────────────────────

  Future<void> _linkExistingRepo() async {
    if (_ownerCtrl.text.isEmpty || _repoCtrl.text.isEmpty) {
      _snack('Please fill Owner and Repo name.');
      return;
    }
    final token = await storageService.getToken();
    if (token == null) { _snack('No token found. Check Settings.'); return; }

    if (!ref.read(gitProvider).isGitRepo) {
      await ref.read(gitProvider.notifier).initRepo(_projectPath!);
    }
    await ref.read(gitProvider.notifier)
        .setRemoteWithToken(_ownerCtrl.text, _repoCtrl.text, token);
    _snack('Repository linked successfully!');
  }

  Future<void> _createNewRepo() async {
    if (_newRepoNameCtrl.text.isEmpty) {
      _snack('Please enter a repository name.');
      return;
    }
    _snack('Creating repository on GitHub...');
    final success = await _githubClient.createRepository(
      _newRepoNameCtrl.text,
      private: _isPrivate,
    );
    if (!success) {
      _snack('Failed — check if the name already exists.');
      return;
    }
    final token = await storageService.getToken();
    if (!ref.read(gitProvider).isGitRepo) {
      await ref.read(gitProvider.notifier).initRepo(_projectPath!);
    }
    await ref.read(gitProvider.notifier)
        .setRemoteWithToken(_githubUsername, _newRepoNameCtrl.text, token!);
    _snack('🎉 Repository created and linked!');
  }

  // ─── Safe push ───────────────────────────────────────────────────────────────

  Future<void> _safePush() async {
    if (_projectPath == null) return;
    _snack('Scanning for secrets before pushing...');
    _logger.info('Pre-push scan started...');

    await ref.read(scannerStateProvider.notifier).scanDirectories([_projectPath!]);

    final secrets = ref
        .read(scannerStateProvider)
        .findings
        .where((f) => f.type != FindingType.recommendation)
        .toList();

    if (secrets.isNotEmpty && mounted) {
      showDialog(
        context: context,
        builder: (ctx) => AlertDialog(
          backgroundColor: const Color(0xFF1E1E1E),
          title: const Row(
            children: [
              Icon(Icons.warning_amber_rounded, color: Colors.redAccent),
              SizedBox(width: 8),
              Text('Security Risk Detected!',
                  style: TextStyle(color: Colors.redAccent, fontSize: 18)),
            ],
          ),
          content: Text(
            'Found ${secrets.length} potential secret(s) in your code.\n'
            'Review them in the Scanner tab before pushing.',
            style: const TextStyle(color: Colors.white70),
          ),
          actions: [
            TextButton(
              onPressed: () => Navigator.pop(ctx),
              child: const Text('Cancel Push',
                  style: TextStyle(color: Colors.grey)),
            ),
            TextButton(
              onPressed: () async {
                Navigator.pop(ctx);
                _snack('Override active. Pushing...');
                _logger.warning('Push override — user acknowledged ${secrets.length} finding(s)');
                await ref.read(gitProvider.notifier).push();
              },
              child: const Text('Push Anyway',
                  style: TextStyle(color: Colors.redAccent)),
            ),
          ],
        ),
      );
      return;
    }

    _snack('Scan passed ✓ — pushing to GitHub...');
    await ref.read(gitProvider.notifier).push();
  }

  // ─── Diff viewer ─────────────────────────────────────────────────────────────

  Future<void> _loadDiff(String? filePath) async {
    setState(() {
      _showDiff = true;
      _diffFile = filePath;
    });
    await ref.read(gitProvider.notifier).loadDiff(filePath: filePath);
  }

  // ─── Stash ───────────────────────────────────────────────────────────────────

  Future<void> _stash() async {
    await ref.read(gitProvider.notifier).stash();
    _snack('Changes stashed.');
  }

  Future<void> _stashPop() async {
    await ref.read(gitProvider.notifier).stashPop();
    _snack('Stash restored.');
  }

  // ─── Device Flow OAuth ───────────────────────────────────────────────────────

  Future<void> _showDeviceFlowDialog() async {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (ctx) => _DeviceFlowDialog(
        githubClient: _githubClient,
        onSuccess: (token) async {
          await _storage.write(key: 'github_access_token', value: token);
          _snack('✅ GitHub authentication successful!');
          _logger.success('Device Flow OAuth completed successfully');
        },
      ),
    );
  }

  // ─── Helpers ─────────────────────────────────────────────────────────────────

  void _snack(String msg) {
    if (mounted) {
      ScaffoldMessenger.of(context)
          .showSnackBar(SnackBar(content: Text(msg)));
    }
  }

  @override
  void dispose() {
    _commitMsgCtrl.dispose();
    _ownerCtrl.dispose();
    _repoCtrl.dispose();
    _newRepoNameCtrl.dispose();
    super.dispose();
  }

  // ─── Build ───────────────────────────────────────────────────────────────────

  @override
  Widget build(BuildContext context) {
    final git   = ref.watch(gitProvider);
    final isBusy = git.operation != GitOperation.idle;

    return Scaffold(
      backgroundColor: const Color(0xFF131314),
      body: LayoutBuilder(
        builder: (context, constraints) {
          final isMobile = constraints.maxWidth < 800;

          final mainContent = SingleChildScrollView(
            padding: const EdgeInsets.all(24),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Text(
                  'Workspace Integration',
                  style: TextStyle(
                      fontSize: 26,
                      fontWeight: FontWeight.bold,
                      color: Colors.white),
                ),
                const SizedBox(height: 6),
                const Text(
                  'Manage local code and sync securely with GitHub.',
                  style: TextStyle(color: Colors.grey, fontSize: 13),
                ),
                const SizedBox(height: 28),

                // Project picker
                _buildProjectPicker(),
                const SizedBox(height: 20),

                if (_projectPath != null) ...[
                  // Main workflow: setup or commit
                  if (!git.isGitRepo ||
                      git.remoteUrl.isEmpty ||
                      git.remoteUrl == 'No remote')
                    _buildSetupWizard(isBusy)
                  else ...[
                    _buildCommitFlow(git, isBusy),
                    const SizedBox(height: 20),
                    // Branch management
                    const BranchPanel(),
                    const SizedBox(height: 20),
                    // Stash controls
                    _buildStashRow(isBusy),
                    const SizedBox(height: 20),
                    // Diff viewer
                    if (_showDiff) _buildDiffViewer(git),
                  ],
                ],
              ],
            ),
          );

          final sidebar = Container(
            width: isMobile ? double.infinity : 300,
            decoration: BoxDecoration(
              color: const Color(0xFF1E1E1E),
              border: isMobile
                  ? const Border(
                      top: BorderSide(color: Color(0xFF3C4043)))
                  : const Border(
                      left: BorderSide(color: Color(0xFF3C4043))),
            ),
            padding: const EdgeInsets.all(24),
            child: SingleChildScrollView(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  _buildProfileBadge(),
                  const SizedBox(height: 16),
                  // Device Flow button
                  SizedBox(
                    width: double.infinity,
                    child: OutlinedButton.icon(
                      onPressed: _showDeviceFlowDialog,
                      icon: const Icon(Icons.login, size: 16),
                      label: const Text('Authenticate via GitHub',
                          style: TextStyle(fontSize: 13)),
                      style: OutlinedButton.styleFrom(
                        foregroundColor: const Color(0xFF8AB4F8),
                        side: const BorderSide(
                            color: Color(0xFF8AB4F8), width: 1),
                        padding: const EdgeInsets.symmetric(vertical: 12),
                        shape: RoundedRectangleBorder(
                            borderRadius: BorderRadius.circular(10)),
                      ),
                    ),
                  ),
                  const SizedBox(height: 28),
                  Row(
                    children: const [
                      Icon(Icons.auto_awesome,
                          color: Color(0xFF8AB4F8), size: 16),
                      SizedBox(width: 8),
                      Text('Smart Recommendations',
                          style: TextStyle(
                              fontSize: 15,
                              fontWeight: FontWeight.bold,
                              color: Colors.white)),
                    ],
                  ),
                  const SizedBox(height: 14),
                  _buildDynamicRecommendations(git),
                  if (git.operationOutput.isNotEmpty) ...[
                    const SizedBox(height: 24),
                    const Text('Terminal Output',
                        style: TextStyle(
                            fontSize: 14,
                            fontWeight: FontWeight.bold,
                            color: Colors.white)),
                    const SizedBox(height: 8),
                    Container(
                      width: double.infinity,
                      padding: const EdgeInsets.all(12),
                      decoration: BoxDecoration(
                          color: Colors.black45,
                          borderRadius: BorderRadius.circular(8)),
                      child: SelectableText(
                        git.operationOutput,
                        style: TextStyle(
                          color: git.hasError
                              ? Colors.redAccent
                              : Colors.greenAccent,
                          fontFamily: 'monospace',
                          fontSize: 12,
                        ),
                      ),
                    ),
                  ],
                ],
              ),
            ),
          );

          if (isMobile) {
            return SingleChildScrollView(
              child: Column(children: [mainContent, sidebar]),
            );
          }

          return Row(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Expanded(flex: 2, child: mainContent),
              sidebar,
            ],
          );
        },
      ),
    );
  }

